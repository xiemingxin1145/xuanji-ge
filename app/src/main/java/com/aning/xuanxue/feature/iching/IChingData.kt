    /** 六爻(初->上) 求卦 */
    fun lookup(lines: List<Boolean>): Gua {
        val lower = trigram(lines[0], lines[1], lines[2])
        val upper = trigram(lines[3], lines[4], lines[5])
        return table[upper + lower] ?: Gua("未知", "")
    }

    /** 三枚铜钱摇一爻：正面=3 反面=2，合 6老阴(动) 7少阳 8少阴 9老阳(动) */
    fun tossLine(rnd: () -> Int): Yao {
        val sum = rnd() + rnd() + rnd() // 每枚 2 或 3
        return when (sum) {
            6 -> Yao(false, true)   // 老阴 -> 变阳
            7 -> Yao(true, false)   // 少阳
            8 -> Yao(false, false)  // 少阴
            else -> Yao(true, true) // 9 老阳 -> 变阴
        }
    }
